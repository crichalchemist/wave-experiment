"""
HF Jobs training script generator for DPO (Direct Preference Optimization).

Generates a self-contained training script (with ALL code inline) and
launches it on HF Jobs GPU instances via ``huggingface_hub.run_uv_job()``.

The generated script:
- Downloads DPO data from ``crichalchemist/detective-llm-dpo-data``
- Fine-tunes ``deepseek-ai/DeepSeek-R1-Distill-Qwen-7B`` with LoRA adapters
- Uses TRL's DPOTrainer with configurable beta
- Logs metrics via Trackio
- Pushes the LoRA adapter to ``crichalchemist/detective-llm-dpo-adapter``

Pattern follows ``scripts/train_welfare_classifier_hf_job.py``.
"""
from __future__ import annotations

import logging
import os
from typing import Dict, Tuple

logger = logging.getLogger(__name__)

# ============================================================================
# Hardware options for HF Jobs
# ============================================================================

HARDWARE_OPTIONS: Dict[str, str] = {
    "t4-small": "T4 Small -- quick experiments (~$0.06/hr)",
    "a10g-small": "A10G Small -- production training (~$0.75/hr)",
    "a10g-large": "A10G Large -- large batch training (~$1.50/hr)",
    "a100-large": "A100 Large -- 7B model DPO training (~$3.15/hr)",
}

# ============================================================================
# Training script generator
# ============================================================================


def get_training_script(
    epochs: int,
    lr: float,
    batch_size: int,
    beta: float = 0.1,
    lora_rank: int = 16,
    data_source: str = "both",
) -> str:
    """Return a complete, self-contained Python script string for HF Jobs.

    The script includes PEP 723 inline dependencies, LoRA configuration,
    DPOTrainer setup, Trackio logging, and Hub push.

    Parameters are baked into the script via f-string interpolation.
    All dict/set literals inside the generated code use doubled braces.

    Parameters:
        epochs: Number of training epochs.
        lr: Learning rate (e.g. 5e-7).
        batch_size: Per-device training batch size.
        beta: DPO beta parameter controlling deviation from reference policy.
        lora_rank: LoRA rank (r parameter).
        data_source: Which data split to use -- "constitutional", "legal", or
                     "both" (default).
    """
    # NOTE ON BRACE ESCAPING:
    # This is a Python f-string.  To produce a literal { in the output,
    # write {{ in the source.  To produce a literal } in the output,
    # write }} in the source.  Parameters like {epochs} are substituted.
    return f'''\
# /// script
# dependencies = [
#     "torch>=2.0.0",
#     "transformers>=4.40.0",
#     "trl>=0.8.0",
#     "peft>=0.10.0",
#     "datasets>=2.18.0",
#     "huggingface-hub>=0.25.0",
#     "trackio",
#     "accelerate>=0.26.0",
#     "bitsandbytes>=0.43.0",
# ]
# ///
"""Train DPO adapter (DeepSeek-R1-Distill-Qwen-7B + LoRA) -- HF Jobs GPU version.

Downloads preference data from Hub, fine-tunes with DPO via TRL,
pushes LoRA adapter to crichalchemist/detective-llm-dpo-adapter.

Auto-generated by scripts/train_dpo_hf_job.py.
"""
import json
import logging
import os
from pathlib import Path

import torch
from datasets import load_dataset, concatenate_datasets
from huggingface_hub import hf_hub_download
from peft import LoraConfig, TaskType, get_peft_model
from transformers import AutoModelForCausalLM, AutoTokenizer, BitsAndBytesConfig
from trl import DPOConfig, DPOTrainer

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

DATA_REPO_ID = "crichalchemist/detective-llm-dpo-data"
BASE_MODEL_ID = "deepseek-ai/DeepSeek-R1-Distill-Qwen-7B"
ADAPTER_REPO_ID = "crichalchemist/detective-llm-dpo-adapter"


# ============================================================================
# Data loading
# ============================================================================

def load_dpo_data(token, data_source):
    """Download and load DPO preference pairs from Hub.

    Each JSONL row has {{"prompt": ..., "chosen": ..., "rejected": ...}}.
    """
    files = []
    if data_source in ("constitutional", "both"):
        files.append("constitutional_pairs.jsonl")
    if data_source in ("legal", "both"):
        files.append("legal_pairs.jsonl")

    all_datasets = []
    for filename in files:
        local_path = hf_hub_download(
            repo_id=DATA_REPO_ID,
            filename=filename,
            repo_type="dataset",
            token=token,
        )
        ds = load_dataset("json", data_files=local_path, split="train")
        logger.info(f"Loaded {{len(ds)}} examples from {{filename}}")
        all_datasets.append(ds)

    if len(all_datasets) == 1:
        dataset = all_datasets[0]
    else:
        dataset = concatenate_datasets(all_datasets)

    logger.info(f"Total DPO examples: {{len(dataset)}}")
    return dataset


# ============================================================================
# Training
# ============================================================================

def main():
    import trackio

    epochs = {epochs}
    lr = {lr}
    batch_size = {batch_size}
    beta = {beta}
    lora_r = {lora_rank}
    lora_alpha = 32
    lora_dropout = 0.05
    data_source = "{data_source}"

    token = os.environ.get("HF_TOKEN")

    # --- Download data from Hub ---
    logger.info("Downloading DPO training data from Hub...")
    dataset = load_dpo_data(token, data_source)

    # Split into train/eval (90/10)
    split = dataset.train_test_split(test_size=0.1, seed=42)
    train_dataset = split["train"]
    eval_dataset = split["test"]

    logger.info(f"Training on {{len(train_dataset)}} examples")
    logger.info(f"Evaluating on {{len(eval_dataset)}} examples")

    # --- Init Trackio ---
    trackio.init(
        project="detective-llm-dpo-training",
        name=f"dpo-r{{lora_r}}-b{{beta}}-{{epochs}}ep",
        config={{
            "model": BASE_MODEL_ID,
            "epochs": epochs,
            "lr": lr,
            "batch_size": batch_size,
            "beta": beta,
            "lora_r": lora_r,
            "lora_alpha": lora_alpha,
            "data_source": data_source,
        }},
    )

    # --- Load tokenizer ---
    logger.info(f"Loading tokenizer from {{BASE_MODEL_ID}}...")
    tokenizer = AutoTokenizer.from_pretrained(BASE_MODEL_ID, token=token)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    # --- Load model with 4-bit quantization ---
    logger.info(f"Loading model from {{BASE_MODEL_ID}} (4-bit quantized)...")
    bnb_config = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_compute_dtype=torch.bfloat16,
        bnb_4bit_use_double_quant=True,
    )
    model = AutoModelForCausalLM.from_pretrained(
        BASE_MODEL_ID,
        quantization_config=bnb_config,
        device_map="auto",
        torch_dtype=torch.bfloat16,
        token=token,
    )

    # --- Configure LoRA ---
    logger.info(f"Configuring LoRA: r={{lora_r}}, alpha={{lora_alpha}}, dropout={{lora_dropout}}")
    peft_config = LoraConfig(
        r=lora_r,
        lora_alpha=lora_alpha,
        lora_dropout=lora_dropout,
        target_modules=["q_proj", "v_proj"],
        task_type=TaskType.CAUSAL_LM,
        bias="none",
    )

    # --- DPO training configuration ---
    training_args = DPOConfig(
        output_dir="/tmp/dpo-adapter-output",
        num_train_epochs=epochs,
        per_device_train_batch_size=batch_size,
        per_device_eval_batch_size=batch_size,
        learning_rate=lr,
        beta=beta,
        warmup_ratio=0.1,
        weight_decay=0.01,
        logging_steps=10,
        eval_strategy="epoch",
        save_strategy="epoch",
        load_best_model_at_end=True,
        metric_for_best_model="eval_loss",
        greater_is_better=False,
        bf16=True,
        gradient_accumulation_steps=4,
        gradient_checkpointing=True,
        max_length=1024,
        remove_unused_columns=False,
    )

    # --- Create DPO trainer ---
    trainer = DPOTrainer(
        model=model,
        ref_model=None,  # PEFT handles implicit reference model
        args=training_args,
        train_dataset=train_dataset,
        eval_dataset=eval_dataset,
        processing_class=tokenizer,
        peft_config=peft_config,
    )

    # --- Train ---
    logger.info("Starting DPO training...")
    try:
        trainer.train()
    except RuntimeError as e:
        if "out of memory" in str(e).lower():
            logger.error("OOM - reducing batch size to 1 and retrying...")
            training_args.per_device_train_batch_size = 1
            training_args.gradient_accumulation_steps = 16
            trainer = DPOTrainer(
                model=model,
                ref_model=None,
                args=training_args,
                train_dataset=train_dataset,
                eval_dataset=eval_dataset,
                processing_class=tokenizer,
                peft_config=peft_config,
            )
            trainer.train()
        else:
            raise

    # --- Evaluate ---
    logger.info("Evaluating final model...")
    metrics = trainer.evaluate()

    logger.info("Final metrics:")
    for key, value in metrics.items():
        if isinstance(value, float):
            logger.info(f"  {{key}}: {{value:.4f}}")
            trackio.log({{key: value}})

    # --- Push adapter to Hub ---
    logger.info(f"Pushing LoRA adapter to {{ADAPTER_REPO_ID}}...")
    trainer.model.push_to_hub(
        ADAPTER_REPO_ID,
        commit_message=f"DPO adapter -- loss={{metrics.get('eval_loss', -1):.4f}}, beta={{beta}}",
        token=token,
    )
    tokenizer.push_to_hub(
        ADAPTER_REPO_ID,
        token=token,
    )

    eval_loss = metrics.get("eval_loss", float("inf"))
    logger.info(f"Training complete! Final eval loss: {{eval_loss:.4f}}")


if __name__ == "__main__":
    main()
'''


# ============================================================================
# Job launcher
# ============================================================================


def launch_dpo_training(
    epochs: int = 1,
    lr: float = 5e-7,
    batch_size: int = 4,
    beta: float = 0.1,
    lora_rank: int = 16,
    hardware: str = "a10g-large",
    timeout: int = 14400,
    data_source: str = "both",
) -> Tuple[str, str]:
    """Launch a DPO training job on HF Jobs.

    Returns:
        (job_id, message) tuple. job_id is the HF Job identifier;
        message is a human-readable status string.
    """
    if hardware not in HARDWARE_OPTIONS:
        return (
            "",
            f"Unknown hardware flavor: {hardware}. "
            f"Choose from: {list(HARDWARE_OPTIONS.keys())}",
        )

    token = os.environ.get("HF_TOKEN")
    if not token:
        return ("", "HF_TOKEN environment variable not set.")

    script = get_training_script(
        epochs=epochs,
        lr=lr,
        batch_size=batch_size,
        beta=beta,
        lora_rank=lora_rank,
        data_source=data_source,
    )

    try:
        import tempfile
        from huggingface_hub import run_uv_job

        # Write script to temp file — run_uv_job expects a file path
        with tempfile.NamedTemporaryFile(
            mode="w", suffix=".py", delete=False, prefix="dpo_training_"
        ) as f:
            f.write(script)
            script_path = f.name

        job = run_uv_job(
            script=script_path,
            flavor=hardware,
            token=token,
            timeout=timeout,
            secrets={"HF_TOKEN": token},
        )
        job_id = getattr(job, "job_id", str(job))
        message = (
            f"DPO training job launched on {hardware} "
            f"({HARDWARE_OPTIONS[hardware]}). "
            f"Job ID: {job_id}"
        )
        return (job_id, message)
    except Exception as e:
        logger.error("Failed to launch DPO training job: %s", e)
        return ("", f"Failed to launch DPO training job: {e}")
