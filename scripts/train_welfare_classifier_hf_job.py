"""
HF Jobs training script generator for the welfare construct classifier.

Generates a self-contained training script (with ALL code inline) and
launches it on HF Jobs GPU instances via ``huggingface_hub.run_uv_job()``.

The generated script:
- Downloads training data from ``crichalchemist/welfare-training-data``
- Fine-tunes ``distilbert-base-uncased`` (8-label multi-label regression)
- Logs metrics via Trackio
- Pushes the final model to ``crichalchemist/welfare-constructs-distilbert``

Pattern follows ``spaces/maninagarden/training.py``.
"""
from __future__ import annotations

import logging
import os
from typing import Dict, Tuple

logger = logging.getLogger(__name__)

# ============================================================================
# Hardware options for HF Jobs
# ============================================================================

HARDWARE_OPTIONS: Dict[str, str] = {
    "t4-small": "T4 Small -- quick experiments (~$0.06/hr)",
    "a10g-small": "A10G Small -- production training (~$0.75/hr)",
    "a10g-large": "A10G Large -- large batch training (~$1.50/hr)",
}

# ============================================================================
# Training script generator
# ============================================================================


def get_training_script(
    epochs: int,
    lr: float,
    batch_size: int,
) -> str:
    """Return a complete, self-contained Python script string for HF Jobs.

    The script includes PEP 723 inline dependencies, the WelfareDataset
    class, compute_metrics, HF Trainer setup, Trackio logging, and Hub push.

    Parameters are baked into the script via f-string interpolation.
    All dict/set literals inside the generated code use doubled braces.
    """
    # NOTE ON BRACE ESCAPING:
    # This is a Python f-string.  To produce a literal { in the output,
    # write {{ in the source.  To produce a literal } in the output,
    # write }} in the source.  Parameters like {epochs} are substituted.
    return f'''\
# /// script
# dependencies = [
#     "torch>=2.0.0",
#     "transformers>=4.40.0",
#     "scikit-learn>=1.3.0",
#     "huggingface-hub>=0.25.0",
#     "trackio",
#     "accelerate>=0.26.0",
# ]
# ///
"""Train welfare construct classifier (DistilBERT, 8-label regression) -- HF Jobs GPU version.

Downloads data from Hub, fine-tunes distilbert-base-uncased, pushes model
to crichalchemist/welfare-constructs-distilbert.

Auto-generated by scripts/train_welfare_classifier_hf_job.py.
"""
import json
import logging
import os
from pathlib import Path

import torch
from torch.utils.data import Dataset
from transformers import (
    DistilBertForSequenceClassification,
    DistilBertTokenizer,
    Trainer,
    TrainingArguments,
)
from sklearn.metrics import mean_absolute_error, mean_squared_error
from huggingface_hub import hf_hub_download

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

CONSTRUCT_NAMES = ["c", "kappa", "j", "p", "eps", "lam_L", "lam_P", "xi"]

DATA_REPO_ID = "crichalchemist/welfare-training-data"
MODEL_REPO_ID = "crichalchemist/welfare-constructs-distilbert"


# ============================================================================
# Inline WelfareDataset
# ============================================================================

class WelfareDataset(Dataset):
    """Multi-label welfare construct dataset."""

    def __init__(self, jsonl_path: str, tokenizer, max_length: int = 512):
        self.data = []
        with open(jsonl_path, "r") as f:
            for line in f:
                self.data.append(json.loads(line))

        self.tokenizer = tokenizer
        self.max_length = max_length

        logger.info(f"Loaded {{len(self.data)}} examples from {{jsonl_path}}")

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        item = self.data[idx]

        # Tokenize
        encoding = self.tokenizer(
            item["text"],
            truncation=True,
            max_length=self.max_length,
            padding="max_length",
            return_tensors="pt",
        )

        # Create label tensor [8] with scores [0,1]
        labels = torch.tensor(
            [item["scores"][construct] for construct in CONSTRUCT_NAMES],
            dtype=torch.float,
        )

        return {{
            "input_ids": encoding["input_ids"].squeeze(),
            "attention_mask": encoding["attention_mask"].squeeze(),
            "labels": labels,
        }}


# ============================================================================
# Inline compute_metrics
# ============================================================================

def compute_metrics(eval_pred):
    """Compute regression metrics for multi-label continuous scores."""
    predictions, labels = eval_pred

    # Convert logits to probabilities [0,1]
    predictions = torch.sigmoid(torch.tensor(predictions)).numpy()

    # Overall metrics
    mae = mean_absolute_error(labels.flatten(), predictions.flatten())
    mse = mean_squared_error(labels.flatten(), predictions.flatten())

    # Per-construct metrics
    per_construct = {{}}
    for i, name in enumerate(CONSTRUCT_NAMES):
        per_construct[f"mae_{{name}}"] = mean_absolute_error(
            labels[:, i], predictions[:, i]
        )

    return {{
        "mae": mae,
        "mse": mse,
        **per_construct,
    }}


# ============================================================================
# Training
# ============================================================================

def main():
    import trackio

    epochs = {epochs}
    lr = {lr}
    batch_size = {batch_size}

    token = os.environ.get("HF_TOKEN")

    # --- Download data from Hub ---
    logger.info("Downloading training data from Hub...")
    train_path = hf_hub_download(
        repo_id=DATA_REPO_ID,
        filename="welfare_training_split_train.jsonl",
        repo_type="dataset",
        token=token,
    )
    val_path = hf_hub_download(
        repo_id=DATA_REPO_ID,
        filename="welfare_training_split_val.jsonl",
        repo_type="dataset",
        token=token,
    )

    # --- Init Trackio ---
    trackio.init(
        project="welfare-classifier-training",
        name=f"distilbert-{{epochs}}ep-{{lr:.0e}}",
        config={{
            "model": "distilbert-base-uncased",
            "num_labels": 8,
            "epochs": epochs,
            "lr": lr,
            "batch_size": batch_size,
        }},
    )

    # --- Load tokenizer and model ---
    logger.info("Loading tokenizer and model...")
    tokenizer = DistilBertTokenizer.from_pretrained("distilbert-base-uncased")
    model = DistilBertForSequenceClassification.from_pretrained(
        "distilbert-base-uncased",
        num_labels=8,
        problem_type="multi_label_classification",
    )

    # --- Load datasets ---
    logger.info("Loading datasets...")
    train_dataset = WelfareDataset(train_path, tokenizer)
    val_dataset = WelfareDataset(val_path, tokenizer)

    logger.info(f"Training on {{len(train_dataset)}} examples")
    logger.info(f"Validating on {{len(val_dataset)}} examples")

    # --- Training configuration ---
    training_args = TrainingArguments(
        output_dir="/tmp/welfare-classifier-output",
        num_train_epochs=epochs,
        per_device_train_batch_size=batch_size,
        per_device_eval_batch_size=32,
        learning_rate=lr,
        warmup_steps=100,
        weight_decay=0.01,
        logging_steps=50,
        eval_strategy="epoch",
        save_strategy="epoch",
        load_best_model_at_end=True,
        metric_for_best_model="eval_loss",
        greater_is_better=False,
        fp16=torch.cuda.is_available(),
    )

    # --- Create trainer ---
    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=train_dataset,
        eval_dataset=val_dataset,
        compute_metrics=compute_metrics,
    )

    # --- Train ---
    logger.info("Starting training...")
    try:
        trainer.train()
    except RuntimeError as e:
        if "out of memory" in str(e).lower():
            logger.error("OOM - reducing batch size to 8 and retrying...")
            training_args.per_device_train_batch_size = 8
            trainer = Trainer(
                model=model,
                args=training_args,
                train_dataset=train_dataset,
                eval_dataset=val_dataset,
                compute_metrics=compute_metrics,
            )
            trainer.train()
        else:
            raise

    # --- Evaluate ---
    logger.info("Evaluating final model...")
    metrics = trainer.evaluate()

    logger.info("Final metrics:")
    for key, value in metrics.items():
        if isinstance(value, float):
            logger.info(f"  {{key}}: {{value:.4f}}")
            trackio.log({{key: value}})

    # --- Push to Hub ---
    logger.info(f"Pushing model to {{MODEL_REPO_ID}}...")
    trainer.push_to_hub(
        repo_id=MODEL_REPO_ID,
        commit_message=f"Welfare classifier -- MAE={{metrics.get('eval_mae', -1):.4f}}",
        token=token,
    )
    tokenizer.push_to_hub(
        repo_id=MODEL_REPO_ID,
        token=token,
    )

    overall_mae = metrics.get("eval_mae", 1.0)
    if overall_mae < 0.20:
        logger.info(f"SUCCESS: MAE {{overall_mae:.4f}} < 0.20 (target met)")
    else:
        logger.warning(f"WARNING: MAE {{overall_mae:.4f}} >= 0.20 (target not met)")

    logger.info("Training complete!")


if __name__ == "__main__":
    main()
'''


# ============================================================================
# Job launcher
# ============================================================================


def launch_classifier_training(
    epochs: int = 3,
    lr: float = 2e-5,
    batch_size: int = 16,
    hardware: str = "t4-small",
    timeout: int = 7200,
) -> Tuple[str, str]:
    """Launch a welfare classifier training job on HF Jobs.

    Returns:
        (job_id, message) tuple. job_id is the HF Job identifier;
        message is a human-readable status string.
    """
    if hardware not in HARDWARE_OPTIONS:
        return (
            "",
            f"Unknown hardware flavor: {hardware}. "
            f"Choose from: {list(HARDWARE_OPTIONS.keys())}",
        )

    token = os.environ.get("HF_TOKEN")
    if not token:
        return ("", "HF_TOKEN environment variable not set.")

    script = get_training_script(
        epochs=epochs,
        lr=lr,
        batch_size=batch_size,
    )

    try:
        import tempfile
        from huggingface_hub import run_uv_job

        # Write script to temp file — run_uv_job expects a file path
        with tempfile.NamedTemporaryFile(
            mode="w", suffix=".py", delete=False, prefix="welfare_classifier_"
        ) as f:
            f.write(script)
            script_path = f.name

        job = run_uv_job(
            script=script_path,
            flavor=hardware,
            token=token,
            timeout=timeout,
        )
        job_id = getattr(job, "job_id", str(job))
        message = (
            f"Welfare classifier training job launched on {hardware} "
            f"({HARDWARE_OPTIONS[hardware]}). "
            f"Job ID: {job_id}"
        )
        return (job_id, message)
    except Exception as e:
        logger.error("Failed to launch training job: %s", e)
        return ("", f"Failed to launch training job: {e}")
