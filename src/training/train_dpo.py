"""
DPO fine-tuning from constitutional preference pairs.

Loads JSONL generated by generate_preferences.py and trains with
Direct Preference Optimization. DPO embeds the constitutional critique
signal directly into the model weights without a separate reward model.

Supports CPU-only training via LoRA + bf16 + precomputed reference log-probs.
"""

from __future__ import annotations

import json
from dataclasses import dataclass
from pathlib import Path
from typing import Any

# Named constants — no magic numbers
DPO_OUTPUT_DIR: str = "checkpoints/dpo"
DPO_BETA: float = 0.1          # KL regularisation strength
DPO_LR: float = 5e-7           # conservative LR for preference alignment
DPO_EPOCHS: int = 1            # 1 epoch is typical for DPO
DPO_BATCH_SIZE: int = 1        # batch_size=1 for CPU training (memory)
DPO_GRAD_ACCUM: int = 8        # effective batch = 8
DPO_MAX_LENGTH: int = 1024     # max tokens per sequence

# LoRA configuration
LORA_RANK: int = 16
LORA_ALPHA: int = 32
LORA_DROPOUT: float = 0.05
LORA_TARGET_MODULES: tuple[str, ...] = ("q_proj", "v_proj")

# Default model for DPO training
DEFAULT_MODEL_ID: str = "deepseek-ai/DeepSeek-R1-Distill-Qwen-7B"

# Module-level import with fallback so patch("src.training.train_dpo.DPOConfig", ...)
# and patch("src.training.train_dpo.DPOTrainer", ...) bind correctly in tests
# while avoiding ImportError when torch/trl are absent in the dev env.
try:
    from trl import DPOConfig, DPOTrainer
except ImportError:  # torch not available in dev env
    DPOConfig = None   # type: ignore[assignment,misc]
    DPOTrainer = None  # type: ignore[assignment,misc]

try:
    from peft import LoraConfig, TaskType
except ImportError:
    LoraConfig = None  # type: ignore[assignment,misc]
    TaskType = None    # type: ignore[assignment,misc]


@dataclass(frozen=True)
class PreferenceSample:
    """One DPO training sample, loaded from JSONL preference pairs."""

    instruction: str
    rejected: str
    chosen: str


def load_preference_pairs(path: str) -> list[PreferenceSample]:
    """
    Load preference pairs from JSONL file.

    Each line: {"instruction": str, "rejected": str, "chosen": str}
    Raises FileNotFoundError if path does not exist.
    """
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"Preference JSONL not found: {path}")
    samples = []
    with p.open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rec = json.loads(line)
            samples.append(
                PreferenceSample(
                    instruction=rec["instruction"],
                    rejected=rec["rejected"],
                    chosen=rec["chosen"],
                )
            )
    return samples


def preference_pairs_to_dataset(samples: list[PreferenceSample]) -> Any:
    """Convert PreferenceSample list to HuggingFace Dataset for DPOTrainer.

    DPOTrainer expects columns: prompt, chosen, rejected (all strings).
    """
    from datasets import Dataset

    return Dataset.from_dict({
        "prompt": [s.instruction for s in samples],
        "chosen": [s.chosen for s in samples],
        "rejected": [s.rejected for s in samples],
    })


def build_lora_config() -> Any:
    """Build LoRA configuration for parameter-efficient DPO training."""
    if LoraConfig is None:
        raise ImportError("peft package required for LoRA training")
    return LoraConfig(
        r=LORA_RANK,
        lora_alpha=LORA_ALPHA,
        lora_dropout=LORA_DROPOUT,
        target_modules=list(LORA_TARGET_MODULES),
        task_type=TaskType.CAUSAL_LM,
        bias="none",
    )


def build_dpo_trainer(
    model: object,
    tokenizer: object,
    train_dataset: object,
    eval_dataset: object = None,
) -> object:
    """Build a trl DPOTrainer configured for constitutional preference learning.

    Does not call .train() — caller controls when training starts.
    eval_dataset is optional; pass None to omit validation during training.
    """
    config = DPOConfig(
        output_dir=DPO_OUTPUT_DIR,
        beta=DPO_BETA,
        learning_rate=DPO_LR,
        num_train_epochs=DPO_EPOCHS,
        per_device_train_batch_size=DPO_BATCH_SIZE,
        gradient_accumulation_steps=DPO_GRAD_ACCUM,
        max_length=DPO_MAX_LENGTH,
        precompute_ref_log_probs=True,  # saves ~14GB by freeing ref model
        report_to="none",
        bf16=False,  # set True only when CUDA available
        logging_steps=1,
        save_strategy="epoch",
    )
    return DPOTrainer(
        model=model,
        args=config,
        train_dataset=train_dataset,
        eval_dataset=eval_dataset,
        peft_config=build_lora_config(),
        processing_class=tokenizer,
    )
